// Create an array using the map function who seeach 
// element is equal to the original element plus 5.

// Define the original array
const originalArray = [1, 2, 3, 4, 5];

// Create a new array where each element is the original element plus 5
const newArray = originalArray.map(x => x + 5);

// Output the new array
console.log(newArray);

//output is [6, 7, 8, 9, 10]
